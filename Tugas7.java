    private void bUbahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bUbahActionPerformed
        // TODO add your handling code here:
        try {
        // Get the Document from JTextPane
        javax.swing.text.Document doc = AreaKota.getDocument();
        
        // Define start and end indices
        int start = 0;
        int end = doc.getLength(); // Assuming you want to replace the entire content
        
        // Remove the text from the document
        doc.remove(start, end - start);
        
        // Insert the new text
        doc.insertString(start, eUbah.getText() + "\n", null);
    } catch (javax.swing.text.BadLocationException e) {
        e.printStackTrace();
    }
    }//GEN-LAST:event_bUbahActionPerformed

