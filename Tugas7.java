    private void bHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bHapusActionPerformed
        // TODO add your handling code here:
        javax.swing.text.Document doc = AreaKota.getDocument();
    
    try {
        // Get the text from the document
        String text = doc.getText(0, doc.getLength());
        int endIndex = text.indexOf("\n");
        
        if (endIndex != -1) {
            // Remove the text up to the first newline
            doc.remove(0, endIndex + 1);
        } else {
            // Remove all text if no newline is found
            doc.remove(0, doc.getLength());
        }
    } catch (javax.swing.text.BadLocationException e) {
        e.printStackTrace(); // Handle the exception
    }

    // Remove first item from JComboBox
    if (cMakanan.getItemCount() > 0) { 
        cMakanan.removeItemAt(0);
    }

    // Remove first item from ListModel
    if (ListModel.getSize() > 0) { 
        ListModel.remove(0);
    }
    }//GEN-LAST:event_bHapusActionPerformed

